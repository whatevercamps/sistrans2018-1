CREATE TABLE PROPUESTAS (
ID NUMBER GENERATED by default on null as IDENTITY, 
ID_OPERADOR NUMBER NOT NULL, 
TIPO NUMBER NOT NULL,
NOMBRE VARCHAR2(80 BYTE) NOT NULL, 
CONSTRAINT PK_PROPUESTA PRIMARY KEY(ID),
CONSTRAINT FK_OPERADORPROPUESTA FOREIGN KEY (ID_OPERADOR) REFERENCES OPERADORES(ID),
CONSTRAINT CK_TIPOPROPUESTA CHECK (TIPO = 1 OR TIPO = 2 OR TIPO = 3 OR TIPO = 4 OR TIPO = 5 OR TIPO = 6)
);
